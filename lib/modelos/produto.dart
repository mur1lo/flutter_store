class Produto {
  final int id;
  final String nome;
  final String descricao;
  final double preco;
  final String urlImagem;
  final String categoria;

  Produto({
    required this.id,
    required this.nome,
    required this.descricao,
    required this.preco,
    required this.urlImagem,
    required this.categoria,
  });
}
